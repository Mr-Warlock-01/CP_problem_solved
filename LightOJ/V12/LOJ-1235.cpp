void xxx(int pos, int ans){
    if(pos==(n/2)){mp[ans]=1; return;}
    xxx(pos+1, ans);
    xxx(pos+1, ans+arr[pos]);
    xxx(pos+1, ans+arr[pos]+arr[pos]);
}

void yyy(int pos, int ans){
    if(pos==n){v.pb(ans); return;}
    yyy(pos+1, ans);
    yyy(pos+1, ans+arr[pos]);
    yyy(pos+1, ans+arr[pos]+arr[pos]);
}






