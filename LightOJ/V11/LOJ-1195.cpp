ll powMod(ll x, ll n, ll M){ll res=1; while(n){if(n&1){res=(res*x)%M;} x=(x*x)%M; n>>=1;} return res;}

const ll b1= 1949313259;  
const ll b2= 1997293877;  
const ll m1= 2091573227;
const ll m2= 2117566807;
int pos;
pair<pair<int,int>,int>dfs(string &s, int lev, ll base_1, ll base_2){
    int sz=1;
    ll hash_1=0,hash_2=0;
    while(1){
        if(s[pos]=='1'){
            pos++;
            auto z= dfs(s, lev+1, (base_1*b1)%m1, (base_2*b2)%m2);
            sz+=z.ss;
            hash_1=(hash_1+z.ff.ff)%m1;
            hash_2=(hash_2+z.ff.ss)%m2;
        }
        else{
            pos++;
            break;
        }
    }
    ll zzz1=(sz*base_1)%m1;
    ll zzz2=(sz*base_2)%m2;
    hash_1=powMod(zzz1,hash_1,m1);
    hash_2=powMod(zzz2,hash_2,m2);

    return {{hash_1,hash_2},sz};
}


