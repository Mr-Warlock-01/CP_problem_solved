vector<pair<int,int>>factor;

void factorize(int n){
    if(n%2==0){
        int cnt=1;
        n/=2;
        while(n%2==0){cnt++; n/=2;}
        factor.pb({2,cnt});
    }
    for(int i=3; i*i<=n; i+=2){
        if(n%i==0){
            n/=i;
            int cnt=1;
            while(n%i==0){cnt++; n/=i;}
            factor.pb({i,cnt});
        }
    }
    if(n!=1){factor.pb({n,1});}
}

