bool f=0;


void dfs(int x, int val){
    if(x==n){
        f=(val!=0);
        return;
    }
    for(auto [u,v]:mp[x]){
        dfs(x+1,val^u);
        if(f==1){
            ans.pb(v);
            break;
        }
    }
}


void clr(int n){
    for(int i=0; i<n; i++){
        mp[i].clear();
    }
    ans.clear();
    f=0;
}
