struct custom_hash {
    static uint64_t splitmix64(uint64_t x){
        x+=0x9e3779b97f4a7c15;  x=(x^(x>>30))*0xbf58476d1ce4e5b9;   x=(x^(x>>27))*0x94d049bb133111eb;
        return x^(x>>31);
    }
    size_t operator()(uint64_t x) const{
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};
//#define int ll

const int N=2750140;
vector<int>prime;
int great_div[N];
gp_hash_table<int, int,custom_hash>mp;
vector<int>ans;

void seive(){
    prime.pb(0);
    bitset<N>p;
    p.set();
    p[0]=p[1]=0;
    for(ll i=2; i<N; i++){
        if(p[i]==1){
            prime.pb(i);
            for(ll j=i*i; j<N; j+=i){
                p[j]=0;
                if(great_div[j]==0){
                    great_div[j]=j/i;
                }
            }
        }
    }
}


int bs(int val){
    int l=1, r=199999;
    int ans;
    while(l<=r){
        int mid=(l+r)/2;
        if(prime[mid]==val){ans=mid; break;}
        else if(prime[mid]>val){r=mid-1;}
        else{l=mid+1;}
    }
    return ans;
}


void solve(int T){
    seive();
    int n;  cin>>n;
    vector<int>v(2*n);  cin>>v;
    for(int i=0; i<2*n; i++){mp[v[i]]++;}
    sort(rall(v));

    for(int i=0; i<2*n; i++){
        if(mp[v[i]]>0){
            if(great_div[v[i]]==0){
                int x=bs(v[i]);
                mp[x]--;
                ans.pb(x);
            }
            else{
                mp[great_div[v[i]]]--;
                ans.pb(v[i]);
            }
            mp[v[i]]--;
        }
    }
    assert(ans.size()==n);
    for(int i=0; i<n; i++){cout<<ans[i]<<" ";}
}




