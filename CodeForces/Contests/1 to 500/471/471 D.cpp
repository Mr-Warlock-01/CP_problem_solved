vector<int>build_lps(vector<int>&pat) {
    int sz=pat.size();  
    vector<int>lps(sz);
    for(int i=1; i<sz; i++){
        int k=lps[i-1];
        while((k>0) && (pat[i] != pat[k])){k=lps[k-1];}
        if(pat[i]==pat[k]){k++;}
        lps[i]=k;
    }
return lps;
}

int kmp(vector<int>&txt, vector<int>&pat, vector<int>&lps) {
    int sz_a=txt.size(), sz_b=pat.size();
    int ans=0;
    int k=0;
    for(int i=0; i<sz_a; i++){
        while((k>0) && (txt[i]!=pat[k])){k=lps[k-1];}
        if(txt[i]==pat[k]){k++;}
        if(k==sz_b){
            ans++;
            k=lps[k-1];
        }
    }
    return ans;
}





