int sum_1_to_n(int x){
    int ans=x*(x+1);
    ans>>=1;
    return ans;
}

bool check(int health, int dis, int day){
    int rest=day-dis;
    int z=dis/(rest+1);
    int z_mod=dis%(rest+1);
    health+=rest;
    health-=(sum_1_to_n(z+1))*z_mod;
    health-=(sum_1_to_n(z))*(rest-z_mod+1);
    if(health<1){return 0;}
    return 1;
}

void solve(int T){
    int  h,d;   cin>>h>>d;
    int ans=LLMax;
    int l=d, r=2*d;
    while(l<=r){
        int mid=(l+r)/2;
        bool ok=check(h, d, mid);
        if(ok){
            ans=mid; r=mid-1;
        }
        else{
            l=mid+1;
        }
    }    
    cout<<ans<<endl;
}   


    

