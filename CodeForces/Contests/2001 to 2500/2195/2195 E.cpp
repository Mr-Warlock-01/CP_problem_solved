const int N=3e5+7;

vector<int>graph[N];
int parent[N];
int sz[N];
int ans[N];

void clr(int n){
    for(int i=0; i<n; i++){
        graph[i].clear();
        sz[i]=-1;
        ans[i]=0;
        parent[N];
    }
}

int dfs(int x, int par){
    int cnt=0;
    for(int u:graph[x]){
        cnt+=dfs(u,x);
    }
    sz[x]=cnt;
    return cnt+2;
}

void ans_dfs(int x, int p){
    ans[x]=(sz[x]+ans[p]+1)%MOD;
    for(int u:graph[x]){
        ans_dfs(u,x);
    }
}


void solve(int T){
    int n;  cin>>n;
    clr(n+3);
    for(int i=1; i<=n; i++){
        int a,b;    cin>>a>>b;
        if(a==0 && b==0){continue;}
        graph[i].pb(a);
        graph[i].pb(b);
        parent[a]=i;
        parent[b]=i;
    }

    dfs(1,0);

    ans_dfs(1,0);
    for(int i=1; i<=n; i++){
        cout<<ans[i]<<" ";
    }


} 



    

