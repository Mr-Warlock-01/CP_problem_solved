const int N=2e5+7;

int arr[N];
int pre[N];
int pre_mn[N],suf_mx[N]; 
int ans=0;
int n;

void _find(int x){
    for(int i=1; i<=n; i++){
        pre[i]= ((arr[i]<x)? -1 : 1 );
    }

    pre[0]=0;
    for(int i=1; i<=n; i++){
        pre[i]+=pre[i-1];
    }

    pre_mn[0]=0;
    for(int i=1; i<=n; i++){
        pre_mn[i]=min(pre_mn[i-1], pre[i]);
    }

    suf_mx[n]=pre[n];
    for(int i=n-1; i>0; i--){
        suf_mx[i]=max(suf_mx[i+1], pre[i]);
    }

    for(int i=1; i<=n ;i++){
        if(pre_mn[i-1]<=suf_mx[i]){
            ans=max(ans, x-arr[i]);
        }
    }
}


