int sumOfDigits(int n){
    if(n<=0){return 0;}
    int res=0;
    for(int p=1; p<=n; p*=10){
        int pp10= p*10;
        int high= n/pp10;
        int cur = (n/p)%10;
        int low = n%p;
        res += (high*p*45) + (((cur*(cur-1))/2)*p) + (cur*(low+1));
    }
    return res;
}


int check(int x){
    int ans=0;
    int z=0;
    int ok=9;
    int sz=1;
    while(1){
        if(x<=ok){
            ans+=sz*(x-z);
            break;
        }
        ans+=sz*(ok-z);
        z=ok;
        ok*=10;
        ok+=9;
        sz++;
    }
    return ans;
}


void solve(int T){
    int k;  cin>>k;
    int l=1, r=1e15;
    int n;

    while(l<=r){
        int mid=(l+r)/2;
        if(check(mid)<=k){
            n=mid; l=mid+1;
        }
        else{r=mid-1;}
    }
    int ans=sumOfDigits(n);
    int need=k-check(n);
    vector<int>v;
    int x=n+1;
    while(x>0){
        v.pb(x%10);
        x/=10;
    }
    for(int i=v.size()-1; i>=v.size()-need; i--){
        ans+=v[i];
    }
    cout<<ans<<endl;
}       
  
 
 
 
