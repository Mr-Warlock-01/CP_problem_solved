struct custom_hash {
    static uint64_t splitmix64(uint64_t x){
        x+=0x9e3779b97f4a7c15;  x=(x^(x>>30))*0xbf58476d1ce4e5b9;   x=(x^(x>>27))*0x94d049bb133111eb;
        return x^(x>>31);
    }
    size_t operator()(uint64_t x) const{
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};


#define int ll

const int N=1e5+7;
int arr[N];
gp_hash_table<int, int,custom_hash>mp;




inline ll eular_MOD(ll x, ll m) {
    if(x<m){return x;}
    return (x%m)+m;
}

ll powMod(ll x, ll n, ll M) {
    ll res=1;
    while(n){
        if(n&1){res=eular_MOD(res*x, M);}
        x=eular_MOD(x*x, M);
        n>>=1;
    }
    return res;
}



ll phi(ll n){
    int nn=n;
    if(mp[n]!=0){return mp[n];}
    ll ans=n; 
    for(ll i=2; i*i<=n; i++){ 
        if(n%i==0){
            ans/=i; ans*=(i-1); 
            while(n%i==0){n/=i;}
        }
    }
    if(n>1){ans/=n; ans*=(n-1);}
    return mp[nn]=ans;
}



int cal(int l, int r, int M){
    if(M==1){return 1;}
    if(l==r){return eular_MOD(arr[l], M);}
    int z=cal(l+1, r, phi(M));
    return powMod(arr[l], z, M);
}



