const int N=2005;
int arr[N];
int cnt[30][N];
vector<int>pos[30];

int mx=0;
int ans=1;
int n;

void clr(){
    for(int i=0; i<=mx; i++){pos[i].clear();}

    for(int i=0; i<=mx; i++){
        for(int j=0; j<=n; j++){
            cnt[i][j]=0;
        }
    }
    ans=1; mx=0; 
}

void solve(int T){
    cin>>n;
    for(int i=1; i<=n; i++){
        cin>>arr[i];
        mx=max(mx,arr[i]);
    }

    for(int i=1; i<=n; i++){
        pos[arr[i]].pb(i);
        for(int k=1; k<=mx; k++){
            cnt[k][i]+=cnt[k][i-1];
        }
        cnt[arr[i]][i]++;
    }
    for(int i=1; i<=mx; i++){
        int L=0, R=pos[i].size()-1;
        while(L<R){
            int l_idx=pos[i][L];
            int R_idx=pos[i][R];
            for(int Z=1; Z<=mx; Z++){
                ans=max(ans,2*(L+1)+cnt[Z][R_idx-1]-cnt[Z][l_idx]);
            }
            L++,R--;
        }
    }
    cout<<ans<<endl;
    clr();


}





